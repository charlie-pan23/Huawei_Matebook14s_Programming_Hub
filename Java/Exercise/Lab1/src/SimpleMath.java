public class SimpleMath {
    public static void main(String[] args) {
        int num1 = 10, num2= 3;
        double real1 = 10.0 , real2= 3.0;
        System.out.println("num1 + num2 = " + (num1+num2));
        System.out.println("num1 / num2 = " + (num1 / num2));
        System.out.println("real1 + real2 = " + (real1 + real2));
        System.out.println("real1 / real2 = " + (real1 / real2));
    }

}
